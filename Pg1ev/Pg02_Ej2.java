////Realice un programa que lea dos números enteros y muestre su producto si éste es mayor que 10; en caso contrario indicará al usuario que el producto no es mayor que 10. 
public class Pg02_Ej2 {

	public static void main(String[] args) {
		int a=7;
		int b=5;
		int p=a*b;
		if (p>10) System.out.println("El producto es: "+p);
		else System.out.println("El producto no es mayor que 10.");
		
		a=2;
		b=3;
		p=a*b;
		if (p>10) System.out.println("El producto es: "+p);
		else System.out.println("El producto no es mayor que 10.");
	
	
	}

}
